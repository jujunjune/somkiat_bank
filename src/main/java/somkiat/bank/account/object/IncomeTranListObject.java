package somkiat.bank.account.object;

public class IncomeTranListObject {

	private String transDate;
	private String tarnAmount;
	private String tranAccount;
	
	public String getTransDate() {
		return transDate;
	}
	public void setTransDate(String transDate) {
		this.transDate = transDate;
	}
	public String getTarnAmount() {
		return tarnAmount;
	}
	public void setTarnAmount(String tarnAmount) {
		this.tarnAmount = tarnAmount;
	}
	public String getTranAccount() {
		return tranAccount;
	}
	public void setTranAccount(String tranAccount) {
		this.tranAccount = tranAccount;
	}

}
